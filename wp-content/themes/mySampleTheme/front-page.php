                <h3 class="py-4 resume-pheader">Resume Do’s and Don’ts</h3>
                <div class="row">
                    <div class="col-sm-6">
                        <ul class="resume-dos">
                            <li><i class="fas fa-check-circle fa-lg" style="color:#090057"></i><span>Do have your name
                                    at the top with one
                                    email address, one phone number, and address (if you must) in the header.</span>
                            </li>
                            <li><i class="fas fa-check-circle fa-lg" style="color:#090057"></i><span>Describe the
                                    current position you’re in with bullet points starting with present tense verbs like
                                    “Identify,” “Research,” “Write,” etc.</span></li>
                            <li><i class="fas fa-check-circle fa-lg" style="color:#090057"></i><span>Do make sure
                                    you include certifications, internships, clubs you were a part of, awards you
                                    received in college, etc.</span></li>
                            <li><i class="fas fa-check-circle fa-lg" style="color:#090057"></i><span>Do show your soft
                                    skills in your bullet point descriptions of your duties at previous
                                    jobs.</span></li>
                            <li><i class="fas fa-check-circle fa-lg" style="color:#090057"></i><span>Expand any
                                    abbreviations in school names people won’t know. (UC Berkeley and IIT Bombay
                                    are fine; other three/four-letter school acronyms are not.) When in doubt,
                                    expand.</span></li>
                        </ul>
                    </div>
                    <div class="col-sm-6">
                        <ul class="resume-dos">
                            <li><i class="fas fa-times-circle fa-lg" style="color:#090057"></i><span>Do not have your
                                    birth-date, marital
                                    status, parents’ names, social security or passport
                                    or other identification numbers, sexuality, or anything else.</span>
                            </li>
                            <li><i class="fas fa-times-circle fa-lg" style="color:#090057"></i><span>Do not have an
                                    “Objective” section at the top of your resume. It’s outdated and not
                                    helpful unless the job requires it.</span></li>
                            <li><i class="fas fa-times-circle fa-lg" style="color:#090057"></i><span>Do not include
                                    anything before college unless it’s professional experience </span></li>

                            <li><i class="fas fa-times-circle fa-lg" style="color:#090057"></i><span>Do not put your

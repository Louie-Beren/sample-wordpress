                <h3 class="py-4 resume-pheader">FAQs about Resumes</h3>
                <h4><i class="fas fa-caret-down"></i><a data-bs-toggle="collapse" href="#FAQ1" role="button"
                        aria-expanded="false" aria-controls="FAQ1">
                        How long should your resume be?
                    </a></h4>

                <div class="collapse" id="FAQ1">
                    <p>If you’re just out of school or have been working for &lt 10 years, one page.</p>
                    <p>
                        If there are no requirements and you have a lot of experience or extraordinary circumstances, up
                        to two pages. That’s it. My resume is only two pages and you’ll be surprised how much you can
                        fit in this length.</p>
                    <p>

                        If you are sure your industry accepts more pages (such as when you apply for an academic
                        position at a university), then it can be longer.
                        This should be obvious, but fill the page. You can talk about relevant clubs you were a part of
                        at university or you can add more bullet points to what you did at your jobs. But do not have
                        half the page blank.
                    </p>
                </div>
                <h4><i class="fas fa-caret-down"></i><a data-bs-toggle="collapse" href="#FAQ2" role="button"
                        aria-expanded="false" aria-controls="FAQ2">
                        Should your resume be creative?
                    </a></h4>

                <div class="collapse" id="FAQ2">
                    <p>
                        This depends on how much is in your resume and the industry. There is so much in my resume that
                        I can’t make it that creative and keep it within two pages. Prioritize information not
                        creativity. However, if you have the space to make it creative, a resume that has some color can
                        really help you stand out.
                    </p>

                </div>
